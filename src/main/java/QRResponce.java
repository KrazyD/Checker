public class QRResponce {

    public QRResponce() {
    }

    private String type;
    private ResponseSymbol[] symbol;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public ResponseSymbol[] getSymbol() {
        return symbol;
    }

    public void setSymbol(ResponseSymbol[] symbol) {
        this.symbol = symbol;
    }
}
